	public void setITEM_TYPE(String iTEM_TYPE) {
		ITEM_TYPE = iTEM_TYPE;
	}
	public String getITEM_CONTENT() {
		return ITEM_CONTENT;
	}
	public void setITEM_CONTENT(String iTEM_CONTENT) {
		ITEM_CONTENT = iTEM_CONTENT;
	}
